# variable type
# integer - number
# string - text
# float/double - decimal (with points)
# bool or boolean - value can be either true (1) or false (0) e.g. is_light_on = 1/0


# Variable Assignment in Python works just like Math except the position is reversed.
# g(x) = x + 1
# f(x) = x + 3
# x = 1
# f(g(x)) -> f(2) -> 5

age = 34  # type => integer - automatically known by python
my_age = "34"  # type => string - inside quote

name = "Biniam"  # type => string

height = 1.74  # type => float/double
